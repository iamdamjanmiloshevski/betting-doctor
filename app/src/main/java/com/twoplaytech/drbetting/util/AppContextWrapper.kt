package com.twoplaytech.drbetting.util

import android.annotation.TargetApi
import android.content.Context
import android.content.ContextWrapper
import android.content.res.Configuration
import android.os.Build
import com.twoplaytech.drbetting.persistence.IPreferences
import com.twoplaytech.drbetting.persistence.SharedPreferencesManager
import java.util.*

/*
    Author: Damjan Miloshevski 
    Created on 4/5/21 1:31 PM
*/
class AppContextWrapper (base: Context, private val preferencesManager: SharedPreferencesManager) : ContextWrapper(base) {
        fun wrap(context: Context): ContextWrapper {
            var context: Context = context
            val language = preferencesManager.getString(IPreferences.KEY_LOCALE)
            val config: Configuration = context.resources.configuration
            var sysLocale: Locale? = null
            sysLocale = if (Build.VERSION.SDK_INT > Build.VERSION_CODES.N) {
                getSystemLocale(config)
            } else {
                getSystemLocaleLegacy(config)
            }
            if (language != "" && sysLocale.language != language) {
                val locale = Locale(language)
                Locale.setDefault(locale)
                if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
                    setSystemLocale(config, locale)
                } else {
                    setSystemLocaleLegacy(config, locale)
                }
            }
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
                context = context.createConfigurationContext(config)
            } else {
                context.resources
                    .updateConfiguration(config, context.resources.displayMetrics)
            }
            return AppContextWrapper(context, preferencesManager)
        }

        fun getSystemLocaleLegacy(config: Configuration): Locale {
            return config.locale
        }

        @TargetApi(Build.VERSION_CODES.N)
        fun getSystemLocale(config: Configuration): Locale {
            return config.getLocales().get(0)
        }

        fun setSystemLocaleLegacy(config: Configuration, locale: Locale) {
            config.locale = locale
        }

        @TargetApi(Build.VERSION_CODES.N)
        fun setSystemLocale(config: Configuration, locale: Locale?) {
            config.setLocale(locale)
        }
}
